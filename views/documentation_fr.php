
<h1>Configuration du Plugin</h1>

<ul class="doc-list">
    <li>Créer une entité dans le back office de votre blog dans le menu « Ajouter une entité ».</li>
    <li>L'entité créée correspond à l'entité qui est posée sur votre mapping.</li>
    <li><b>Chaque entité WordPress posée sur le mapping possède un ID unique et devra être créée dans la
            configuration du plugin.</b></li>
</ul>

<br>
<h1>Configuration d’une entité</h1>

<p>Dans le menu création d’entité, voici à quoi correspondent les différents champs :</p>

<p><b>Nom de l'entité</b></p>

<ul class="doc-list">
    <li>Renseigner le nom que vous souhaitez donner à votre entité.</li>
</ul>

<p><b>Identifiant de l'entité :</b></p>

<ul class="doc-list">
    <li>Pour remplir cette case, rendez-vous sur votre mapping et récupérez le code "Identifiant à indiquer dans votre plugin" et le coller.</li>
</ul>

<p><b>Publier en mode :</b></p>

<p>Définir le mode de publication :</p>
    <ul class="doc-list">
        <li>Publication Directe</li>
        <li>Publication en mode « Brouillon »</li>
        <li>Publication « En attente de relecture »</li>
        <li>Publication « Privée »</li>
    </ul>

<p><b>Image :</b></p>

<ul class="doc-list">
    <li>Publier l’image de l’article en tant que « Image à la une ».</li>
    <li>Publier l’image dans l’article.</li>
    <li>Publier l’image en tant que « Image à la une » et dans l’article.</li>
</ul>

<p><b>Afficher les articles dans une fenêtre modale :</b></p>
<p>Permet d’ouvrir l’article publié dans une fenêtre modale :</p>
<ul class="doc-list">
    <li>Vos visiteurs peuvent lire l’article publié sur le site source à travers votre site.</li>
    <li>A la fermeture de la « Fenêtre Modale » les visiteurs restent sur votre site.</li>
    <li>Permet de conserver le trafic sur votre site web et de réduire le taux de rebond sur votre site.</li>
</ul>

<p><b>Inclure les balises de liens canoniques :</b></p>

<ul class="doc-list">
    <li>Permet d’inclure, ou non les balises de lien canoniques.</li>
</ul>

<p><b>Ne pas suivre les liens (nofollow) :</b></p>

<ul class="doc-list">
    <li>Permet au moteur de recherche de suivre les liens.</li>
</ul>

<p><b>Libellé suite d'article :</b></p>

<ul class="doc-list">
    <li>Libellé de la phrase permettant de lire la suite de l’article.</li>
    <li>Cette phrase sera présente dans l’article en elle-même et non sur les boutons de votre site.</li>
</ul>
